/**
 * Reads List.xlsx (single column of names), writes CSV + lib/roomSharingOptions.generated.ts
 * Usage: node scripts/import-room-sharing-list.mjs [path/to/List.xlsx]
 */
import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const xlsxPath =
  process.argv[2] ||
  path.join(process.env.HOME || "", "Downloads", "List.xlsx");

function decodeEntities(s) {
  return s
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#x([0-9a-fA-F]+);/g, (_, h) =>
      String.fromCodePoint(parseInt(h, 16))
    )
    .replace(/&#(\d+);/g, (_, d) => String.fromCodePoint(parseInt(d, 10)));
}

function extractSharedStrings(xml) {
  const out = [];
  const siRe = /<si>([\s\S]*?)<\/si>/g;
  let m;
  while ((m = siRe.exec(xml))) {
    const inner = m[1];
    const parts = [...inner.matchAll(/<t[^>]*>([\s\S]*?)<\/t>/g)].map((x) =>
      decodeEntities(x[1])
    );
    out.push(parts.join(""));
  }
  return out;
}

function escapeTsString(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\r/g, "");
}

function csvEscape(cell) {
  if (/[",\n]/.test(cell)) {
    return `"${cell.replace(/"/g, '""')}"`;
  }
  return cell;
}

function main() {
  if (!fs.existsSync(xlsxPath)) {
    console.error("File not found:", xlsxPath);
    process.exit(1);
  }

  const xml = execFileSync("unzip", ["-p", xlsxPath, "xl/sharedStrings.xml"], {
    encoding: "utf8",
    maxBuffer: 10 * 1024 * 1024,
  });

  let names = extractSharedStrings(xml).map((s) => s.trim()).filter(Boolean);

  const extra = "Other / will confirm separately";
  if (!names.includes(extra)) {
    names = [...names, extra];
  }

  const dataDir = path.join(root, "data");
  fs.mkdirSync(dataDir, { recursive: true });
  const csvPath = path.join(dataDir, "room-sharing-list.csv");
  const csvBody = ["name", ...names.map((n) => csvEscape(n))].join("\n");
  fs.writeFileSync(csvPath, csvBody, "utf8");
  console.log("Wrote", csvPath, "rows:", names.length);

  const tsPath = path.join(root, "lib", "roomSharingOptions.generated.ts");
  const header = `/** Auto-generated by scripts/import-room-sharing-list.mjs — do not edit by hand. */\n\nexport const roomSharingOptions = [\n`;
  const lines = names.map((n) => `  "${escapeTsString(n)}",`).join("\n");
  const footer = `\n] as const;\n`;
  fs.writeFileSync(tsPath, header + lines + footer, "utf8");
  console.log("Wrote", tsPath);
}

main();
